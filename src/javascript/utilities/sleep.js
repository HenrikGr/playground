const delay = 550         // half a second

/**
 * Added this sleep function to be able to simulate a real database
 * @param milliseconds
 */
function sleep(milliseconds) {
  const date = Date.now();
  let currentDate = null;
  do {
    currentDate = Date.now();
  } while (currentDate - date < milliseconds);
}


